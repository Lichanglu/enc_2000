int CharToIndex16x16(char c)
{

	// 如果为小写字母
	if(c >= 'a' && c <= 'z') {
		return (c - 'a' + 10 + 26);
	}

	//如果为数字
	else if(c >= '0' && c <= '9') {
		return (c - '0');
	}
	//如果为大写字母
	else if(c >= 'A' && c <= 'Z') {
		return (c - 'A' + 10);
	} else if(c == ':') {
		return (c - ':' + 64);
	} else if(c == '.') {
		return (c - '.' + 63);
	} else if(c == ' ') {
		return (c - ' ' + 62);
	} else {
		return -1;
	}

	return 1;
}

